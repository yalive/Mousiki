import UIKit

func robotoRegular(size: Int) -> UIFont? {
    return R.font.robotoRegular(size: CGFloat(size))
}

func robotoBold(size: Int) -> UIFont? {
    return R.font.robotoBold(size: CGFloat(size))
}

func robotoMedium(size: Int) -> UIFont? {
    return R.font.robotoMedium(size: CGFloat(size))
}

func robotoLight(size: Int) -> UIFont? {
    return R.font.robotoLight(size: CGFloat(size))
}

func robotoItalic(size: Int) -> UIFont? {
    return R.font.robotoItalic(size: CGFloat(size))
}
